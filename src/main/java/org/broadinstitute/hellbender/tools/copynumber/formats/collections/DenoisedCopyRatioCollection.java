package org.broadinstitute.hellbender.tools.copynumber.formats.collections;

import org.broadinstitute.hellbender.exceptions.UserException;
import org.broadinstitute.hellbender.tools.copynumber.formats.records.LinearNonLocatableCopyRatio;
import org.broadinstitute.hellbender.tools.copynumber.gcnv.GermlineCNVNamingConstants;
import org.broadinstitute.hellbender.utils.tsv.DataLine;
import org.broadinstitute.hellbender.utils.tsv.TableColumnCollection;
import org.broadinstitute.hellbender.tools.copynumber.GermlineCNVCaller;

import java.io.File;
import java.util.function.BiConsumer;
import java.util.function.Function;

/**
 * Collection of denoised copy ratios generated by {@link GermlineCNVCaller}
 *
 * @author Andrey Smirnov &lt;asmirnov@broadinstitute.org&gt;
 */
public class DenoisedCopyRatioCollection extends AbstractSampleRecordCollection<LinearNonLocatableCopyRatio> {

    public DenoisedCopyRatioCollection(final File inputFile) {
        super(inputFile,
                new TableColumnCollection(GermlineCNVNamingConstants.DENOISED_COPY_RATIO_MEAN_TABLE_COLUMN),
                getDenoisedCopyRatioRecordFromDataLineDecoder(),
                getDenoisedCopyRatioRecordToDataLineEncoder());
    }

    /**
     * Generates an instance of {@link LinearNonLocatableCopyRatio} from a {@link DataLine} entry read from a denoised copy
     * ratio file generated by `gcnvkernel`.
     */
    private static Function<DataLine, LinearNonLocatableCopyRatio> getDenoisedCopyRatioRecordFromDataLineDecoder() {
        return dataLine -> {
            try {
                return new LinearNonLocatableCopyRatio(dataLine.getDouble(GermlineCNVNamingConstants.DENOISED_COPY_RATIO_MEAN_TABLE_COLUMN));
            } catch (final IllegalArgumentException ex) {
                throw new UserException.BadInput(String.format("Validation error occurred on line %d of the denoised copy ratio file : ", dataLine.getLineNumber())
                        + ex.getMessage());
            }
        };
    }

    /**
     * Generates an instance of {@link DataLine} from {@link LinearNonLocatableCopyRatio} for writing denoised copy ratio
     * collection to file.
     */
    private static BiConsumer<LinearNonLocatableCopyRatio, DataLine> getDenoisedCopyRatioRecordToDataLineEncoder() {
        return (denoisedCopyRatio, dataLine) -> dataLine.append(denoisedCopyRatio.getDenoisedCopyRatio());
    }

}
